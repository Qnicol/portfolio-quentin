const renderImageCollections = () => {
  document.querySelectorAll("[data-image-collection]").forEach((container) => {
    const items = imageCollections[container.dataset.imageCollection] || [];
    container.innerHTML = items
      .map(
        (item) => `
          <a class="media-card" href="${item.src}" target="_blank" rel="noreferrer">
            <div class="media-card__media">
              <img src="${item.src}" alt="${item.title}" loading="lazy" />
            </div>
            <div class="media-card__body">
              <p class="media-card__type">${item.type}</p>
              <h3>${item.title}</h3>
              <p>${item.description}</p>
            </div>
          </a>
        `
      )
      .join("");
  });
};

const renderVideoCollections = () => {
  document.querySelectorAll("[data-video-collection]").forEach((container) => {
    const items = videoCollections[container.dataset.videoCollection] || [];
    container.innerHTML = items
      .map(
        (item) => `
          <article class="video-card">
            <div class="video-card__media">
              <video controls preload="metadata" playsinline>
                <source src="${item.src}" type="video/mp4" />
              </video>
            </div>
            <div class="video-card__body">
              <p class="media-card__type">${item.type}</p>
              <h3>${item.title}</h3>
              <p>${item.description}</p>
              <div class="video-card__meta">
                <span>${item.size}</span>
                <a href="${item.src}" target="_blank" rel="noreferrer">Ouvrir le fichier</a>
              </div>
            </div>
          </article>
        `
      )
      .join("");
  });
};

renderImageCollections();
renderVideoCollections();
